namespace PZone
{
    /// <summary>
    /// Единицы измерения времени.
    /// </summary>
    public enum TimeUnit
    {
        /// <summary>
        /// Неизвестная единица измерения.
        /// </summary>
        /// <remarks>
        /// Значение используется как значение по умолчанию.
        /// </remarks>
        Unknown,

        /// <summary>
        /// Миллисекунда.
        /// </summary>
        Millisecond,

        /// <summary>
        /// Секунда.
        /// </summary>
        Second,

        /// <summary>
        /// Минута.
        /// </summary>
        Minute,

        /// <summary>
        /// Час.
        /// </summary>
        Hour,

        /// <summary>
        /// День.
        /// </summary>
        Day,

        /// <summary>
        /// Неделя.
        /// </summary>
        Week,

        /// <summary>
        /// Месяц.
        /// </summary>
        Month,

        /// <summary>
        /// Квартал.
        /// </summary>
        Quarter,

        /// <summary>
        /// Год.
        /// </summary>
        Year,

        /// <summary>
        /// Век.
        /// </summary>
        Century
    }
}